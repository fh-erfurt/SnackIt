		<h2>Design</h2>
		Marvin
		<p>Das Layout ist relativ schlicht gehalten, so dass die Produkte in den Vordergrund gerückt werden und die Webseite für jeden leicht verständlich ist und ohne Probleme bedient werden kann. Das Design soll modern sein und an das Metro Design von Windows erinnern. Elemente sind klar abgegrenzt und gewisse Bereiche sind oft durch scharfe Kanten getrennt. Das grau im Hintergrund wirkt modern und bildet einen guten Kontrast zu der Akzentfarbe Orange, welche auch zu gleich ein Eye-Catcher ist. Die gewählte Akzentfarbe ist des Weiteren eine warme Farbe und hat eine gute Synergie zu der Nahrungsmittel Thematik. Die gewählt Font Art ist gut lesbar und nicht aufdringlich. Nahezu alle Elemente besitzen einen Hover-Effekt und signalisieren dem Benutzer, dass er mit diesen interagieren kann.</p>
