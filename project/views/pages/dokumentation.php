		<h2>Inhalt</h2>
		Marvin
		<p>Bei Snackit handelt es sich um einen Online Handel, welcher sich auf den Verkauf von Knabberzeug, sowie Getränke und weitere Nahrungsmittel spezialisiert hat. Der Kunde möchte eine moderne und aufgeräumte Webseite, welche es Nutzern, von jeder Altersgruppe einfach macht, einen Account zu erstellen und eine Bestellung mit seinen Lieblingssnacks abzuschließen. Durch den Online Vertrieb ist es möglich, Produkte anzubieten, welche nicht in jedem Supermarkt zu finden sind. Außerdem können Kunde einfach entspannte Abende planen, ohne vorher mehrere Supermärkte anzulaufen und alle gewünschten Waren zu besorgen.<br>
			Die Kunden haben einen leichten Zugang zu ihren Lieblingssnacks und können sich den Einkauf für geplante Spiele- oder Kinoabende ersparen. Ältere Menschen, welche nicht mehr so mobil sind, wird außerdem die Möglichkeit geben sich ihre Knabbereien selbst zu besorgen, ohne auf Freunde und Familie angewiesen zu sein, welche ihnen bei der Beschaffung helfen müssen.
